# Now we should get at least all possible platforms
extract_unique_platforms() {
    grep -o '"platform":"[^"]*"' json/$1.json \
        | cut -d':' -f2 \
        | tr -d '"' \
        | awk -F ", " '{for(i=1;i<=NF;i++) print $i}' \
        | sort -n \
        | uniq > platforms_ranking/unique/$1_platforms
}

# As there are a lot of modules that have no platform specified, but rather a target,
# we need to search for all those modules now and extract the rank
extract_overall_platforms_and_rankings() {
    mkdir -p platforms_ranking/platform_results/$1
    while IFS= read -r line; do
        ../metasploit-framework/msfconsole -q -x "search type:$1 platform:$line -o platforms_ranking/platform_results/$1/$line.csv; exit"
    done < $2 
}

